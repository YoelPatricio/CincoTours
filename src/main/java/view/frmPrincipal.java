public void activarModulos(List<Integer> perm){
    
    for (int i = 0; i < perm.size(); i++) {
        Integer numMod=perm.get(i);
        if(numMod==1){
            btnRuta1.setEnabled(true);
        }else if (numMod==2) {
            btnHorario2.setEnabled(true);
        }else if (numMod==3) {
            btnBus3.setEnabled(true);
        }else if (numMod==4) {
            btnRol4.setEnabled(true);
        }else if (numMod==5) {
            btnEmpleado5.setEnabled(true);
        }else if (numMod==6) {
            btnUsuario6.setEnabled(true);
        }else if (numMod==7) {
            btnProgramacion7.setEnabled(true);
        }else if (numMod==8) {
            btnControlMov8.setEnabled(true);
        }else if (numMod==9) {
            btnEncomienda9.setEnabled(true);
        }else if (numMod==10) {
            btnVentaBoleto10.setEnabled(true);
        }else if (numMod==11) {
            btnRegistrarBoleto11.setEnabled(true);
        }else if (numMod==12) {
            btnIngreso12.setEnabled(true);
        }else if (numMod==13) {
            btnAflueClie13.setEnabled(true);
        }else if (numMod==14) {
            btnAflueClie14.setEnabled(true);
        }else if (numMod==15) {
            btnConfig15.setEnabled(true);
        }
    }
    
    
    
    
    
}

